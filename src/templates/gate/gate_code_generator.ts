export type GateTemplateTag =
  | 'tribe_permit'
  | 'toll_gate'
  | 'bounty_gate'
  | 'open_permit'
  | 'multi_rule';

export interface GateGeneratorInput {
  templateTag: GateTemplateTag;
  moduleName?: string;
  selection: ChipSelection;
}

const mainFileGenerators: Record<
  GateTemplateTag,
  (selection: ChipSelection) => { filename: string; content: string }
> = {
  tribe_permit: (s) => ({ filename: 'sources/tribe_permit.move', content: generateTribePermitMove(s) }),
  toll_gate:    (s) => ({ filename: 'sources/toll_gate.move', content: generateTollGateMove(s) }),
  bounty_gate:  (s) => ({ filename: 'sources/bounty_gate.move', content: generateBountyGateMove(s) }),
  open_permit:  (s) => ({ filename: 'sources/open_permit.move', content: generateOpenPermitMove(s) }),
  multi_rule:   (s) => ({ filename: 'sources/multi_rule.move', content: generateMultiRuleMove(s) }),
};

export function generateGateFiles(input: GateGeneratorInput): FileMap {
  const moduleName = input.moduleName || DEFAULT_MODULE;
  const gen = mainFileGenerators[input.templateTag];
  const main = gen(input.selection);

  const files: FileMap = {
    'Move.toml': generateMoveToml(moduleName),
    'sources/config.move': replaceModuleName(generateConfigMove(), moduleName),
    [main.filename]: replaceModuleName(main.content, moduleName),
  };

  return files;
}
