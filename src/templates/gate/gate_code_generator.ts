// Template E: Multi-Rule Gate
// ---------------------------------------------------------------------------

function generateMultiRuleMove(selection: ChipSelection): string {
  const accessSnippets = collectSnippets('access', selection);
  const paymentSnippets = collectSnippets('payment', selection);
  const revenueSnippets = collectSnippets('revenue', selection);

  const needsPayment = hasChip('P1', selection);
  const needsTribeConfig = hasChip('A1', selection);

  const tribeConfigTypes = needsTribeConfig
    ? `
public struct TribeConfig has drop, store {
    tribe: u32,
}

public struct TribeConfigKey has copy, drop, store {}
`
    : '';

  const tollConfigTypes = needsPayment
    ? `
public struct TollConfig has drop, store {
    price: u64,
    owner_address: address,
}

public struct TollConfigKey has copy, drop, store {}
`
    : '';

  const paymentParam = needsPayment ? `\n    mut payment: Coin<SUI>,` : '';
  const coinImport = needsPayment
    ? `\nuse sui::coin::{Self, Coin};\nuse sui::sui::SUI;`
    : '';

  const adminFunctions: string[] = [];
  if (needsTribeConfig) {
    adminFunctions.push(`
public fun set_tribe_config(
    extension_config: &mut ExtensionConfig,
    admin_cap: &AdminCap,
    tribe: u32,
) {
    extension_config.set_rule<TribeConfigKey, TribeConfig>(
        admin_cap,
        TribeConfigKey {},
        TribeConfig { tribe },
    );
}`);
  }
  if (needsPayment) {
    adminFunctions.push(`
public fun set_toll_config(
    extension_config: &mut ExtensionConfig,
    admin_cap: &AdminCap,
    price: u64,
    owner_address: address,
) {
    extension_config.set_rule<TollConfigKey, TollConfig>(
        admin_cap,
        TollConfigKey {},
        TollConfig { price, owner_address },
    );
}`);
  }

  return `/// Multi-Rule Gate — generated by EasyAssemblies.
///
/// Combines access control and payment rules in a single gate.
/// Chips: [${selection.enabledChips.join(', ')}]
#[allow(unused_use, unused_variable)]
module smart_gate_extension::multi_rule;

use smart_gate_extension::config::{Self, AdminCap, XAuth, ExtensionConfig};
use sui::clock::Clock;${coinImport}
use world::{character::Character, gate::{Self, Gate}};

// === Errors ===
#[error(code = 0)]
const ENotAllowedTribe: vector<u8> = b"Character tribe not allowed";
#[error(code = 1)]
const ENoTribeConfig: vector<u8> = b"Missing TribeConfig on ExtensionConfig";
#[error(code = 2)]
const EBlockedTribe: vector<u8> = b"Character tribe is blocked";
#[error(code = 3)]
const ENoTollConfig: vector<u8> = b"Missing TollConfig on ExtensionConfig";
#[error(code = 4)]
const EInsufficientPayment: vector<u8> = b"Insufficient payment amount";
#[error(code = 5)]
const EExpiryOverflow: vector<u8> = b"Expiry timestamp overflow";

public struct MultiRuleConfig has drop, store {
    expiry_duration_ms: u64,
}

public struct MultiRuleConfigKey has copy, drop, store {}
${tribeConfigTypes}${tollConfigTypes}
public fun issue_jump_permit(
    extension_config: &ExtensionConfig,
    source_gate: &Gate,
    destination_gate: &Gate,
    character: &Character,${paymentParam}
    clock: &Clock,
    ctx: &mut TxContext,
) {
    // ═══════════ Access chips ═══════════${accessSnippets || `
    // (none selected)`}

    // ═══════════ Payment chips ═══════════${paymentSnippets || `
    // (none selected)`}

    // ═══════════ Revenue chips ═══════════${revenueSnippets || `
    // (none selected)`}

    // ═══════════ Issue permit ═══════════
    assert!(extension_config.has_rule<MultiRuleConfigKey>(MultiRuleConfigKey {}), EExpiryOverflow);
    let mr_cfg = extension_config.borrow_rule<MultiRuleConfigKey, MultiRuleConfig>(MultiRuleConfigKey {});
    let expiry_ms = mr_cfg.expiry_duration_ms;
    let ts = clock.timestamp_ms();
    assert!(ts <= (0xFFFFFFFFFFFFFFFFu64 - expiry_ms), EExpiryOverflow);
    let expires_at_timestamp_ms = ts + expiry_ms;
    gate::issue_jump_permit<XAuth>(
        source_gate,
        destination_gate,
        character,
        config::x_auth(),
        expires_at_timestamp_ms,
        ctx,
    );
}

// === Admin Functions ===
public fun set_multi_rule_config(
    extension_config: &mut ExtensionConfig,
    admin_cap: &AdminCap,
    expiry_duration_ms: u64,
) {
    extension_config.set_rule<MultiRuleConfigKey, MultiRuleConfig>(
        admin_cap,
        MultiRuleConfigKey {},
        MultiRuleConfig { expiry_duration_ms },
    );
}
${adminFunctions.join('\n')}
`;
}

