/**
 * Gate code generator — assembles Move source files from template + chip selections.
 *
 * Unlike Turret (1 template, many chip combos), Gate has multiple templates
 * with structurally different code. The generator dispatches on `templateTag`
 * to pick the right skeleton, then injects chip snippets into marked regions.
 *
 * Produces:
 *   Move.toml              — package manifest
 *   sources/config.move    — shared ExtensionConfig + AdminCap + XAuth (all templates)
 *   sources/<main>.move    — template-specific main module with chip code injected
 */

import type { FileMap } from '../types';
import type { ChipSelection } from '../chip-types';
import { GATE_CHIPS } from './chips';

const DEFAULT_MODULE = 'smart_gate_extension';

function replaceModuleName(source: string, moduleName: string): string {
  if (moduleName === DEFAULT_MODULE) return source;
  return source.replaceAll(DEFAULT_MODULE, moduleName);
}

function collectSnippets(
  category: string,
  selection: ChipSelection,
): string {
  const enabled = GATE_CHIPS.filter(
    c => c.category === category && selection.enabledChips.includes(c.id),
  );
  if (enabled.length === 0) return '';
  return enabled
    .map(chip => {
      const params = selection.chipConfigs[chip.id];
      return chip.codeSnippet(params);
    })
    .join('\n');
}

function hasChip(id: string, selection: ChipSelection): boolean {
  return selection.enabledChips.includes(id);
}

// ---------------------------------------------------------------------------
// Move.toml (shared by all Gate templates)
// ---------------------------------------------------------------------------

function generateMoveToml(moduleName: string): string {
  return `[package]
name = "${moduleName}"
edition = "2024"

[dependencies]
world = { git = "https://github.com/evefrontier/world-contracts.git", subdir = "contracts/world", rev = "v0.0.21" }

[environments]
testnet = "4c78adac"
testnet_internal = "4c78adac"
testnet_utopia = "4c78adac"
testnet_stillness = "4c78adac"
`;
}

// ---------------------------------------------------------------------------
// sources/config.move (shared by all Gate templates)
// ---------------------------------------------------------------------------

function generateConfigMove(): string {
  return `/// Shared configuration for gate extensions — generated by EasyAssemblies.
module smart_gate_extension::config;

use sui::dynamic_field as df;

public struct ExtensionConfig has key {
    id: UID,
}

public struct AdminCap has key, store {
    id: UID,
}

public struct XAuth has drop {}

fun init(ctx: &mut TxContext) {
    let admin_cap = AdminCap { id: object::new(ctx) };
    transfer::transfer(admin_cap, ctx.sender());

    let config = ExtensionConfig { id: object::new(ctx) };
    transfer::share_object(config);
}

public fun has_rule<K: copy + drop + store>(config: &ExtensionConfig, key: K): bool {
    df::exists_(&config.id, key)
}

public fun borrow_rule<K: copy + drop + store, V: store>(config: &ExtensionConfig, key: K): &V {
    df::borrow(&config.id, key)
}

public fun borrow_rule_mut<K: copy + drop + store, V: store>(
    config: &mut ExtensionConfig,
    _: &AdminCap,
    key: K,
): &mut V {
    df::borrow_mut(&mut config.id, key)
}

public fun add_rule<K: copy + drop + store, V: store>(
    config: &mut ExtensionConfig,
    _: &AdminCap,
    key: K,
    value: V,
) {
    df::add(&mut config.id, key, value);
}

public fun set_rule<K: copy + drop + store, V: store + drop>(
    config: &mut ExtensionConfig,
    _: &AdminCap,
    key: K,
    value: V,
) {
    if (df::exists_(&config.id, copy key)) {
        let _old: V = df::remove(&mut config.id, copy key);
    };
    df::add(&mut config.id, key, value);
}

public fun remove_rule<K: copy + drop + store, V: store>(
    config: &mut ExtensionConfig,
    _: &AdminCap,
    key: K,
): V {
    df::remove(&mut config.id, key)
}

public(package) fun x_auth(): XAuth {
    XAuth {}
}
`;
}

// ---------------------------------------------------------------------------
// Template A: Tribe Permit
// ---------------------------------------------------------------------------

function generateTribePermitMove(selection: ChipSelection): string {
  const accessSnippets = collectSnippets('access', selection);

  const needsTribeConfig = hasChip('A1', selection);
  const tribeConfigTypes = needsTribeConfig
    ? `
public struct TribeConfig has drop, store {
    tribe: u32,
    expiry_duration_ms: u64,
}

public struct TribeConfigKey has copy, drop, store {}
`
    : '';

  const adminFunctions = needsTribeConfig
    ? `
// === Admin Functions ===
public fun set_tribe_config(
    extension_config: &mut ExtensionConfig,
    admin_cap: &AdminCap,
    tribe: u32,
    expiry_duration_ms: u64,
) {
    extension_config.set_rule<TribeConfigKey, TribeConfig>(
        admin_cap,
        TribeConfigKey {},
        TribeConfig { tribe, expiry_duration_ms },
    );
}
`
    : '';

  const expirySource = needsTribeConfig
    ? `let expiry_ms = tribe_cfg.expiry_duration_ms;`
    : `let expiry_ms: u64 = 3_600_000;`;

  return `/// Tribe Permit Gate — generated by EasyAssemblies.
///
/// Chips: [${selection.enabledChips.join(', ')}]
#[allow(unused_use)]
module smart_gate_extension::tribe_permit;

use smart_gate_extension::config::{Self, AdminCap, XAuth, ExtensionConfig};
use sui::clock::Clock;
use world::{character::Character, gate::{Self, Gate}};

// === Errors ===
#[error(code = 0)]
const ENotAllowedTribe: vector<u8> = b"Character tribe not allowed";
#[error(code = 1)]
const ENoTribeConfig: vector<u8> = b"Missing TribeConfig on ExtensionConfig";
#[error(code = 2)]
const EExpiryOverflow: vector<u8> = b"Expiry timestamp overflow";
#[error(code = 3)]
const EBlockedTribe: vector<u8> = b"Character tribe is blocked";
${tribeConfigTypes}
public fun issue_jump_permit(
    extension_config: &ExtensionConfig,
    source_gate: &Gate,
    destination_gate: &Gate,
    character: &Character,
    clock: &Clock,
    ctx: &mut TxContext,
) {
    // ═══════════ Access chips ═══════════${accessSnippets || `
    // (none selected)`}

    // ═══════════ Issue permit ═══════════
    ${expirySource}
    let ts = clock.timestamp_ms();
    assert!(ts <= (0xFFFFFFFFFFFFFFFFu64 - expiry_ms), EExpiryOverflow);
    let expires_at_timestamp_ms = ts + expiry_ms;
    gate::issue_jump_permit<XAuth>(
        source_gate,
        destination_gate,
        character,
        config::x_auth(),
        expires_at_timestamp_ms,
        ctx,
    );
}
${adminFunctions}`;
}

